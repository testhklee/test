package com.hk.soup.teacher.dto;

public class TeacherDto {
	
	private int teacherNo;
	private int memberNo;
	private String teacherDd;
	private String teacherCertificate;
	private String teacherPrvideo;
	private String teacherStatus;
	private String memberName;
	private String memberTel;
	private String memberEmail;
	private String memberGrade;
	
	public TeacherDto() {
		super();
	}

	public int getTeacherNo() {
		return teacherNo;
	}

	public void setTeacherNo(int teacherNo) {
		this.teacherNo = teacherNo;
	}

	public int getMemberNo() {
		return memberNo;
	}

	public void setMemberNo(int memberNo) {
		this.memberNo = memberNo;
	}

	public String getTeacherDd() {
		return teacherDd;
	}

	public void setTeacherDd(String teacherDd) {
		this.teacherDd = teacherDd;
	}

	public String getTeacherCertificate() {
		return teacherCertificate;
	}

	public void setTeacherCertificate(String teacherCertificate) {
		this.teacherCertificate = teacherCertificate;
	}

	public String getTeacherPrvideo() {
		return teacherPrvideo;
	}

	public void setTeacherPrvideo(String teacherPrvideo) {
		this.teacherPrvideo = teacherPrvideo;
	}

	public String getTeacherStatus() {
		return teacherStatus;
	}

	public void setTeacherStatus(String teacherStatus) {
		this.teacherStatus = teacherStatus;
	}

	public String getMemberName() {
		return memberName;
	}

	public void setMemberName(String memberName) {
		this.memberName = memberName;
	}

	public String getMemberTel() {
		return memberTel;
	}

	public void setMemberTel(String memberTel) {
		this.memberTel = memberTel;
	}

	public String getMemberEmail() {
		return memberEmail;
	}

	public void setMemberEmail(String memberEmail) {
		this.memberEmail = memberEmail;
	}

	public String getMemberGrade() {
		return memberGrade;
	}

	public void setMemberGrade(String memberGrade) {
		this.memberGrade = memberGrade;
	}
	
	
	
	

}
